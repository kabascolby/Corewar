#include "libft.h"

int		ft_debug(char *s)
{
	if (s && ft_strlen(s) > 0)
	{
		ft_putstr_fd(s, 2);
		ft_putstr_fd("\033[92m <--\n\033[0m", 2);
	}
	return (1);
}
